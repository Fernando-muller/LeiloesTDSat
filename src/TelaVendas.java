public void listarVendidos() {
    try {
        ProdutosDAO dao = new ProdutosDAO();
        ArrayList<ProdutosDTO> lista = dao.listarProdutosVendidos();

DefaultTableModel modelo = (DefaultTableModel) tableV.getModel();
       for (ProdutosDTO p : lista) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getValor()
            });
       
        
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao carregar vendas");
    }
      
}
